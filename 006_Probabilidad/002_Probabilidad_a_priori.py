# Tema - Probabilidad a Priori

# El GPS establece creencias iniciales antes de observar datos

# Probabilidades iniciales (a priori)
P_trafico = 0.7      # experiencia previa: suele haber tráfico
P_no_trafico = 0.3   # pocas veces está despejado

print("== PROBABILIDAD A PRIORI ==")
print(f"P(Tráfico) = {P_trafico}")
print(f"P(No Tráfico) = {P_no_trafico}")

# Simular evidencia (por ejemplo, cámara o sensor)
# El GPS aún no la usa, solo confía en su intuición inicial
print("\n Antes de recibir datos, el GPS confía en su experiencia previa.")
print("   A eso se le llama 'probabilidad a priori'.")
